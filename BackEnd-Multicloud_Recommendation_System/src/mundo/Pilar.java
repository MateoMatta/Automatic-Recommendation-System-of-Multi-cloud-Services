package mundo;

public class Pilar  {


	private String nombreDelPilar;
	
	private Integer valorDelPilar;

	public Pilar(String nombreDelPilar, int valorDelPilar) {
		super();
		this.nombreDelPilar = nombreDelPilar;
		this.valorDelPilar = valorDelPilar;
	}

	public String getNombreDelPilar() {
		return nombreDelPilar;
	}

	public void setNombreDelPilar(String nombreDelPilar) {
		this.nombreDelPilar = nombreDelPilar;
	}

	public int getValorDelPilar() {
		return valorDelPilar;
	}

	public void setValorDelPilar(int valorDelPilar) {
		this.valorDelPilar = valorDelPilar;
	}
	
	
	
}
